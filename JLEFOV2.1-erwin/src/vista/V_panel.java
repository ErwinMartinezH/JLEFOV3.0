    private String generarCadenaAleatoria(int longitud, ArrayList<Character> alf) {
        if (longitud <= 0 || alf.isEmpty()) {
            throw new IllegalArgumentException("Longitud no válida o lista de caracteres vacía");
        }

        java.util.Random random = new java.util.Random();
        StringBuilder sb = new StringBuilder();

        for (int i = 0; i < longitud; i++) {
            int indiceAleatorio = random.nextInt(alf.size());
            char caracterAleatorio = alf.get(indiceAleatorio);
            sb.append(caracterAleatorio);
        }

        return sb.toString();
    }

