    private void jLabel8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel8MouseClicked
        //boton de agregar        
        try {
            cabecera = jTextField1.getText().charAt(0);
            if (Character.isUpperCase(cabecera)) {
                produccion = jTextField2.getText().toCharArray();
                if (produccion.length == 0) {
                    JOptionPane.showMessageDialog(this, "La regla de la producion no puede quedar en blanco\nsi desea utilizar epsilon ingrese el simbolo '?'", "Regla invalida", JOptionPane.ERROR_MESSAGE);
                } else {
                    reglas.add(new Regla2(cabecera, produccion));
                    String newItem = cabecera + " ➜ " + new String(produccion);
                    jComboBox1.addItem(newItem);
                    try {
                        jComboBox1.setSelectedIndex(jComboBox1.getSelectedIndex() + 1);
                    } catch (Exception ex) {
                    }
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField1.setEnabled(true);
                }
            } else {
                JOptionPane.showMessageDialog(this, "El nombre de la producion debe ser una sola letra en mayuscula", "Nombre de producción invalido", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se ingresaron valores correctos", "Datos invalido", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabel8MouseClicked

